package com.sicheng.front.interceptor;

import org.springframework.beans.factory.DisposableBean;
import org.springframework.core.NamedThreadLocal;
import org.springframework.web.servlet.HandlerInterceptor;
import org.springframework.web.servlet.ModelAndView;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 * <p>标题: 获取当前页面执行时间(拦截器)</p>
 * <p>描述: </p>
 * <p>公司: 思程科技 www.sicheng.net</p>
 *
 * @author cl
 * @version 2017年5月24日 上午11:22:53
 */
public class ExecuteTimeInterceptor implements HandlerInterceptor, DisposableBean {

    public static ThreadLocal<Long> threadLocal = new NamedThreadLocal<Long>("ExecuteTimeInterceptor 获取当前页面执行时间(拦截器)");

    public static ThreadLocal<Long> getThreadLocal(){
        return threadLocal;
    }

    @Override
    public boolean preHandle(HttpServletRequest request, HttpServletResponse response, Object handler)
            throws Exception {
        //把刚进去拦截器的时间记录在threadLocal中
        threadLocal.set(System.currentTimeMillis());
        return true;
    }

    @Override
    public void postHandle(HttpServletRequest request, HttpServletResponse response, Object handler,
                           ModelAndView modelAndView) throws Exception {

    }

    @Override
    public void afterCompletion(HttpServletRequest request, HttpServletResponse response, Object handler, Exception ex)
            throws Exception {
    }

    /**
     * Invoked by the containing {@code BeanFactory} on destruction of a bean.
     *
     * @throws Exception in case of shutdown errors. Exceptions will get logged
     *                   but not rethrown to allow other beans to release their resources as well.
     */
    @Override
    public void destroy() throws Exception {
        threadLocal.remove();
    }
}
