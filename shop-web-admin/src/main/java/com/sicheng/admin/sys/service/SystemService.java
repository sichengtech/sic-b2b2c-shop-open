package com.sicheng.admin.sys.service;

import com.sicheng.common.security.Digests;
import com.sicheng.common.service.BaseService;
import com.sicheng.common.shiro.FdpSessionDAOI;
import com.sicheng.common.utils.Encodes;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Propagation;
import org.springframework.transaction.annotation.Transactional;

import java.io.Serializable;

/**
 * 系统管理，安全相关实体的管理类,包括用户、角色、菜单.
 *
 * @author zhaolei
 * @version 2013-12-05
 */
@Service
@Transactional(propagation = Propagation.SUPPORTS)
public class SystemService extends BaseService implements Serializable {

    public static final String HASH_ALGORITHM = "SHA-1";
    public static final int HASH_INTERATIONS = 1024;
    public static final int SALT_SIZE = 8;

    @Autowired
    private FdpSessionDAOI sessionDao;

    public FdpSessionDAOI getSessionDao() {
        return sessionDao;
    }

    /**
     * 验证密码
     *
     * @param plainPassword 明文密码
     * @param password      密文密码
     * @return 验证成功返回true
     */
    public boolean validatePassword(String plainPassword, String password) {
        String plain = Encodes.unescapeHtml(plainPassword);
        byte[] salt = Encodes.decodeHex(password.substring(0, 16));
        byte[] hashPassword = Digests.sha1(plain.getBytes(), salt, HASH_INTERATIONS);
        return password.equals(Encodes.encodeHex(salt) + Encodes.encodeHex(hashPassword));
    }


}
